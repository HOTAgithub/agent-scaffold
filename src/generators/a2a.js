/**
 * A2A Agent Generator
 * Generates Google A2A protocol agent card + handler from agent config
 */

const fs = require('fs');
const path = require('path');

function generateA2A(config, outDir) {
  const a2aDir = path.join(outDir, 'a2a');
  fs.mkdirSync(a2aDir, { recursive: true });

  const tools = (config.tools || []).map(t => {
    const name = typeof t === 'string' ? t : t.name;
    const desc = typeof t === 'string' ? `${name} tool` : (t.description || `${name} tool`);
    return { name, description: desc };
  });

  // Generate agent-card.json (A2A Agent Card spec)
  const agentCard = {
    schema_version: "1.0",
    name: config.name,
    version: config.version,
    description: config.description,
    url: `https://${config.name}.example.com`,
    capabilities: {
      streaming: true,
      push_notifications: false,
      state_transition_history: false
    },
    default_input_modes: ["text/plain"],
    default_output_modes: ["text/plain"],
    skills: tools.map(t => ({
      id: t.name,
      name: t.name,
      description: t.description,
      tags: [config.name],
      examples: [`Use ${t.name} to ${t.description.toLowerCase()}`]
    })),
    authentication: {
      schemes: ["bearer"]
    }
  };

  if ((config.capabilities || []).includes('text-generation')) {
    agentCard.skills.push({
      id: 'chat',
      name: 'Chat',
      description: `Have a conversation with ${config.name}`,
      tags: ['chat', 'conversation'],
      examples: [`Ask ${config.name} a question`]
    });
  }

  fs.writeFileSync(
    path.join(a2aDir, 'agent-card.json'),
    JSON.stringify(agentCard, null, 2)
  );

  // Generate handler.js (A2A task handler)
  const handlerCode = `/**
 * A2A Agent Handler for ${config.name}
 * Generated by @pickaxe/agent-scaffold
 * 
 * Protocol: Agent-to-Agent (A2A) by Google
 */

const express = require('express');

const app = express();
app.use(express.json());

const AGENT_CARD = require('./agent-card.json');

// Agent Card endpoint
app.get('/.well-known/agent-card.json', (req, res) => {
  res.json(AGENT_CARD);
});

// Task handling — implement your logic here
app.post('/tasks', async (req, res) => {
  const { id, message } = req.body;
  
  // TODO: Implement your agent logic
  const response = \`[${config.name}] Received: \${JSON.stringify(message)}\`;
  
  res.json({
    id: id,
    status: { state: 'completed' },
    artifacts: [{
      parts: [{ type: 'text', text: response }]
    }]
  });
});

// Task status
app.get('/tasks/:id', (req, res) => {
  res.json({
    id: req.params.id,
    status: { state: 'completed' }
  });
});

const PORT = process.env.PORT || 3000;
app.listen(PORT, () => {
  console.log(\`${config.name} A2A Agent running on port \${PORT}\`);
});
`;

  fs.writeFileSync(path.join(a2aDir, 'handler.js'), handlerCode);

  // Generate package.json
  const pkg = {
    name: `${config.name}-a2a-agent`,
    version: config.version,
    description: `A2A Agent for ${config.name}`,
    main: 'handler.js',
    scripts: { start: 'node handler.js' },
    dependencies: {
      express: '^4.18.0'
    }
  };
  fs.writeFileSync(path.join(a2aDir, 'package.json'), JSON.stringify(pkg, null, 2));

  // Generate README
  const readme = `# ${config.name} — A2A Agent

Generated by @pickaxe/agent-scaffold

## Install
\`\`\`bash
npm install
\`\`\`

## Run
\`\`\`bash
npm start
\`\`\`

## Endpoints
- \`GET /.well-known/agent-card.json\` — Agent Card
- \`POST /tasks\` — Create task
- \`GET /tasks/:id\` — Get task status

## Skills
${tools.map(t => `- **${t.name}**: ${t.description}`).join('\n')}

## Use with A2A Client
\`\`\`javascript
const { A2AClient } = require('@google/a2a');
const client = new A2AClient('http://localhost:3000');
const task = await client.createTask({ message: 'Hello!' });
\`\`\`
`;
  fs.writeFileSync(path.join(a2aDir, 'README.md'), readme);

  console.log(`  📦 A2A Agent → ${a2aDir}/`);
  console.log(`     agent-card.json, handler.js, package.json, README.md`);
}

module.exports = { generateA2A };
